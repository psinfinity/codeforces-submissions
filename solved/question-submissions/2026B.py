import sys
input = sys.stdin.readline #input

def solve():
    n = int(input())
    arr = list(map(int,input().split()))
    max_diff = 1
    if n%2==0:
        i=0
        while i<n:
            max_diff = max(arr[i+1]-arr[i],max_diff)
            i+=2
    else:
        i=0
        max_diff = 1e19
        while i<n:
            max_diff_candidate=1
            j=0
            while j<n:
                if j == i:
                    j+=1
                if j<n:
                    max_diff_candidate = max(arr[j + 1] - arr[j], max_diff_candidate)
                j+=2
            i+=2
            max_diff = min(max_diff_candidate,max_diff)

    print(max_diff)
for _ in range(int(input())):
    solve()