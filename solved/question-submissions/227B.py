import sys
input = sys.stdin.readline #input

def solve():
    n = int(input())
    arr = list(map(int,input().split()))
    m = int(input())
    queries = list(map(int,input().split()))
    vasya = {}
    petya = {}
    for i in range(n):
        petya[arr[i]]=n-i
        if arr[i] not in vasya:
            vasya[arr[i]]=i+1
    vas = 0
    pet = 0
    for query in queries:
        vas+=vasya[query]
        pet+=petya[query]
    print(vas, pet)

solve()