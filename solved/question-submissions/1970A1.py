given = list(input())
n = len(given)

balance = []
bal = 0
for bracket in given:
    balance.append(bal)
    if bracket == '(':
        bal+=1
    else:
        bal-=1

bal_find = {}
for i in range(n):
    if balance[i] not in bal_find:
        bal_find[balance[i]] = []
    bal_find[balance[i]].append(i)

ans = []
for key in bal_find.keys():
    for i in bal_find[key][::-1]:
        ans.append(given[i])

print(''.join(ans))